# This script performs basic arithmetic operations (addition, subtraction, and multiplication)
# on two predefined numbers and prints the results.

# Assign the specified values to the two variables
number1 = 10
number2 = 5

# --- Perform Operations ---

# 1. Addition
sum_result = number1 + number2

# 2. Subtraction (number1 - number2)
difference_result = number1 - number2

# 3. Multiplication
product_result = number1 * number2

# --- Print Results in Human-Readable Format ---
# The format requested is: [operation] of [number1] and [number2] is [result]

# Print Addition result
print(f"Addition of {number1} and {number2} is {sum_result}")

# Print Subtraction result
print(f"Subtraction of {number1} and {number2} is {difference_result}")

# Print Multiplication result
print(f"Multiplication of {number1} and {number2} is {product_result}")