# This script calculates the simple interest (I = P * R * T) based on predefined values.

# 1. Define the variables for the calculation
principal = 1000  # P: The initial investment amount ($1000)
rate = 0.05       # R: The annual interest rate (5% as a decimal)
time = 3          # T: The time in years (3 years)

# 2. Calculate the simple interest
# Formula: Interest = Principal * Rate * Time
interest = principal * rate * time

# 3. Print the calculated interest in the spcecified format
print(f"The simple interest is: {interest}")